// ── GET /api/events/chaincode  →  Chaincode Event Listener ───────────────────
//
//  This route uses Server-Sent Events (SSE) to stream chaincode events
//  to the browser in real time.
//
//  WHAT ARE CHAINCODE EVENTS?
//  When a chaincode function runs, it can emit a named event (e.g. "Transfer").
//  After the transaction is committed to the ledger, the Fabric Gateway
//  delivers those events to subscribers.
//
//  network.getChaincodeEvents() returns an async iterable of event objects.
//  Each event has: chaincodeId, eventName, transactionId, payload.
//
//  SSE (Server-Sent Events) lets the server push messages to the browser
//  over a long-lived HTTP connection — perfect for streaming live events.
//
app.get('/api/events/chaincode', async (req, res) => {
    // SSE headers — tell browser to keep the connection open
    res.setHeader('Content-Type', 'text/event-stream');
    res.setHeader('Cache-Control', 'no-cache');
    res.setHeader('Connection', 'keep-alive');
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.flushHeaders();

    let eventStream = null;

    const send = (data) => {
        // SSE format: "data: <json>\n\n"
        res.write(`data: ${JSON.stringify(data)}\n\n`);
    };

    try {
        const { network } = await getContract();

        // getChaincodeEvents() subscribes to ALL events emitted by this chaincode
        // startBlock: 0n means "replay from the beginning of the ledger"
        eventStream = await network.getChaincodeEvents(config.chaincodeName, {
            startBlock: BigInt(0),
        });

        send({ type: 'connected', message: 'Listening for chaincode events...' });

        // Iterate over the async stream — each iteration yields one event
        for await (const event of eventStream) {
            const payload = event.payload
                ? JSON.parse(Buffer.from(event.payload).toString())
                : null;

            send({
                type:          'chaincode_event',
                eventName:     event.eventName,
                transactionId: event.transactionId,
                payload,
                timestamp:     new Date().toISOString(),
            });
        }
    } catch (err) {
        if (!res.writableEnded) {
            send({ type: 'error', message: err.message });
        }
    } finally {
        // Always close the event stream when the client disconnects
        if (eventStream) {
            eventStream.close();
        }
    }

    // Clean up when the browser closes the SSE connection
    req.on('close', () => {
        if (eventStream) eventStream.close();
    });
});

