// ── Fabric Network Configuration ──────────────────────────────────────────────
//
//  These paths point to the crypto material generated by the test-network.
//  After running `./network.sh up createChannel -ca`, your fabric-samples
//  directory will have these files.
//
//  IMPORTANT: Update FABRIC_SAMPLES_PATH to match your actual path!
//
// Auto-detect fabric-samples location.
// Checks common locations in order; the first one that exists wins.
// You can always override by setting the FABRIC_SAMPLES_PATH env variable.
function detectFabricSamplesPath() {
    if (process.env.FABRIC_SAMPLES_PATH) return process.env.FABRIC_SAMPLES_PATH;

    const candidates = [
        // GitHub Codespaces — fabric-samples cloned inside the workspace repo
        path.join('/workspaces', ...(process.env.CODESPACE_NAME
            ? [process.env.CODESPACE_NAME]
            : (process.env.PWD || '').split(path.sep).filter(Boolean).slice(0, 3)
        ), 'fabric-samples'),
        // Any /workspaces/* subfolder (scan one level deep)
        ...( fs.existsSync('/workspaces')
            ? fs.readdirSync('/workspaces')
                .map(d => path.join('/workspaces', d, 'fabric-samples'))
            : []
        ),
        // Standard home-directory clone
        path.join(process.env.HOME, 'fabric-samples'),
        // Current working directory
        path.join(process.cwd(), 'fabric-samples'),
        // One level up from cwd
        path.join(process.cwd(), '..', 'fabric-samples'),
    ];

    for (const candidate of candidates) {
        try {
            // A valid fabric-samples has a test-network folder inside
            if (fs.statSync(path.join(candidate, 'test-network')).isDirectory()) {
                return candidate;
            }
        } catch { /* not found, try next */ }
    }

    // Fall back to home — checkPrerequisites() will report the missing files
    return path.join(process.env.HOME, 'fabric-samples');
}

