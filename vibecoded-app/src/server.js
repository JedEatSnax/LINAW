// ── Fabric Connection (singleton — reused across requests) ────────────────────
//
//  We create ONE gRPC connection and ONE Gateway per server process.
//  Creating these is expensive; reusing them is efficient.
//
//  LAZY INITIALISATION: We do NOT connect at server startup. We wait until
//  the first API call arrives. This means the web server starts fine even if
//  the Fabric network is not yet running — the UI can show a helpful "not
//  connected" state rather than a raw crash.
//
let gatewayClient = null;   // the @hyperledger/fabric-gateway Gateway object
let grpcClient    = null;   // the underlying gRPC channel

/**
 * Reads the first file in a directory.
 * Fabric stores certs and keys with auto-generated filenames, so we just
 * grab whichever file is there rather than hard-coding the exact filename.
 */
function readFirstFileInDir(dirPath) {
    const files = fs.readdirSync(dirPath);
    if (files.length === 0) throw new Error(`No files found in ${dirPath}`);
    return fs.readFileSync(path.join(dirPath, files[0]));
}

/**
 * Checks whether the crypto files generated by the test-network exist.
 * Returns a structured diagnostic object so the UI can show exactly
 * which step is missing (network not started vs chaincode not deployed, etc.)
 *
 * The test-network generates these files when you run:
 *   ./network.sh up createChannel -ca
 */
function checkPrerequisites() {
    const checks = [
        {
            label: 'Peer TLS certificate',
            path:  config.tlsCertPath,
            hint:  'Run: ./network.sh up createChannel -ca',
            exists: false,
        },
        {
            label: 'User signing certificate',
            path:  config.certDirectoryPath,
            hint:  'Run: ./network.sh up createChannel -ca',
            exists: false,
            isDir: true,
        },
        {
            label: 'User private key',
            path:  config.keyDirectoryPath,
            hint:  'Run: ./network.sh up createChannel -ca',
            exists: false,
            isDir: true,
        },
    ];

    let allPresent = true;
    for (const check of checks) {
        try {
            const stat = fs.statSync(check.path);
            // For directories, also verify they contain at least one file
            if (check.isDir) {
                const files = fs.readdirSync(check.path);
                check.exists = files.length > 0;
            } else {
                check.exists = stat.isFile();
            }
        } catch {
            check.exists = false;
        }
        if (!check.exists) allPresent = false;
    }

    return { allPresent, checks, fabricSamplesPath: FABRIC_SAMPLES_PATH };
}

/**
 * Creates a gRPC channel to the Fabric peer, secured with TLS.
 *
 * gRPC is the transport protocol that Fabric uses internally.
 * Think of it like HTTP but binary, faster, and streaming-capable.
 * The TLS certificate ensures we're talking to the right peer.
 */
async function newGrpcConnection() {
    const tlsRootCert    = fs.readFileSync(config.tlsCertPath);
    const tlsCredentials = grpc.credentials.createSsl(tlsRootCert);

    return new grpc.Client(
        config.peerEndpoint,
        tlsCredentials,
        {
            // This tells gRPC which hostname to expect in the peer's TLS cert.
            // Needed when connecting via localhost but the cert says peer0.org1.example.com
            'grpc.ssl_target_name_override': config.peerHostAlias,
        }
    );
}

/**
 * Creates a Fabric Gateway connection.
 *
 * The Gateway is your main API handle. It wraps the gRPC connection
 * and adds Fabric-specific logic: transaction proposals, endorsement,
 * ordering, commit waiting, and event listening.
 *
 * Identity  = "who are you?"  → your X.509 certificate
 * Signer    = "prove it"      → your private key used to sign proposals
 * Hash      = "which hash fn" → SHA-256 (standard for Fabric)
 */
async function newGateway(client) {
    const certificate   = readFirstFileInDir(config.certDirectoryPath);
    const privateKeyPem = readFirstFileInDir(config.keyDirectoryPath);
    const privateKey    = crypto.createPrivateKey(privateKeyPem);

    return connect({
        client,                                     // the gRPC channel
        identity: { mspId: config.mspId, credentials: certificate },
        signer: signers.newPrivateKeySigner(privateKey),
        hash: hash.sha256,
    });
}

/**
 * Returns (or lazily creates) the singleton Gateway.
 * All API routes call this to get a contract reference.
 *
 * If the crypto files don't exist yet (network not started), this throws
 * with a clear message. The caller catches it and returns a 503 with
 * actionable instructions.
 *
 * If a previous connection attempt failed, we reset the singleton so the
 * next request tries again — this lets you start the network AFTER the
 * web server is already running, and the app reconnects automatically.
 */
async function getContract() {
    if (!gatewayClient) {
        // Check prerequisites first — gives a much clearer error than
        // letting the fs.readFileSync throw a raw ENOENT.
        const pre = checkPrerequisites();
        if (!pre.allPresent) {
            const missing = pre.checks
                .filter(c => !c.exists)
                .map(c => `  ✗ ${c.label}`)
                .join('\n');
            throw new Error(
                `Fabric test-network crypto material not found.\n\n` +
                `Missing files:\n${missing}\n\n` +
                `Fix: cd ~/fabric-samples/test-network && ` +
                `./network.sh up createChannel -ca`
            );
        }

        try {
            grpcClient    = await newGrpcConnection();
            gatewayClient = await newGateway(grpcClient);
        } catch (err) {
            // Reset so the next request retries instead of reusing a broken state
            grpcClient    = null;
            gatewayClient = null;
            throw err;
        }
    }

    // network  = a channel (ledger + smart contracts)
    // contract = a specific chaincode on that channel
    const network  = gatewayClient.getNetwork(config.channelName);
    const contract = network.getContract(config.chaincodeName);
    return { contract, network };
}

