// ── Health / Status ───────────────────────────────────────────────────────────
//
//  Returns the connection status AND a preflight check so the UI can
//  distinguish between "network not started" and "peer unreachable".
//
app.get('/api/status', async (req, res) => {
    const preflight = checkPrerequisites();

    // If crypto files are missing, we know exactly why we can't connect --
    // report it clearly without attempting a gRPC connection that would crash.
    if (!preflight.allPresent) {
        return res.status(503).json({
            status: 'no_crypto',
            error:  'Fabric test-network crypto files are missing. Has the network been started?',
            preflight,
            config: {
                channel:   config.channelName,
                chaincode: config.chaincodeName,
                mspId:     config.mspId,
                peer:      config.peerEndpoint,
            },
        });
    }

    try {
        // Crypto files exist — attempt the actual gRPC connection
        await getContract();
        res.json({
            status: 'connected',
            preflight,
            config: {
                channel:   config.channelName,
                chaincode: config.chaincodeName,
                mspId:     config.mspId,
                peer:      config.peerEndpoint,
            },
        });
    } catch (err) {
        // Files exist but peer is unreachable (e.g. Docker containers stopped)
        res.status(503).json({
            status: 'peer_unreachable',
            error:  err.message,
            preflight,
            config: {
                channel:   config.channelName,
                chaincode: config.chaincodeName,
                mspId:     config.mspId,
                peer:      config.peerEndpoint,
            },
        });
    }
});


