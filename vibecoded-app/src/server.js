// ── GET /api/assets  →  GetAllAssets ─────────────────────────────────────────
//
//  contract.evaluateTransaction() = READ ONLY
//
//  "Evaluate" means:
//    1. Send the request to the peer
//    2. Peer simulates the chaincode and returns the result
//    3. Result is returned to us — NOT written to the ledger
//    4. No ordering, no consensus — just a local ledger query
//
//  Use evaluate for anything that reads data (no side effects).
//
app.get('/api/assets', async (req, res) => {
    try {
        const { contract } = await getContract();

        // evaluateTransaction sends to peer and returns chaincode response bytes
        const resultBytes = await contract.evaluateTransaction('GetAllAssets');

        // The chaincode returns JSON bytes; we decode and parse them
        const result = JSON.parse(Buffer.from(resultBytes).toString());
        res.json({ success: true, data: result });
    } catch (err) {
        res.status(500).json({ success: false, error: err.message });
    }
});

// ── GET /api/assets/:id  →  ReadAsset ────────────────────────────────────────
app.get('/api/assets/:id', async (req, res) => {
    try {
        const { contract } = await getContract();

        // Pass arguments after the function name — they map to chaincode params
        const resultBytes = await contract.evaluateTransaction('ReadAsset', req.params.id);
        const result = JSON.parse(Buffer.from(resultBytes).toString());
        res.json({ success: true, data: result });
    } catch (err) {
        res.status(404).json({ success: false, error: err.message });
    }
});

// ── POST /api/assets  →  CreateAsset ─────────────────────────────────────────
//
//  contract.submitTransaction() = WRITE (state-changing)
//
//  "Submit" means:
//    1. Client sends a transaction PROPOSAL to the endorsing peer(s)
//    2. Peer simulates it and returns an ENDORSEMENT (signed result)
//    3. Client sends the endorsed proposal to the ORDERER
//    4. Orderer batches it into a block and sends to all peers
//    5. Peers COMMIT the block to their copy of the ledger
//    6. submitTransaction() returns only after step 5 — the ledger is updated
//
//  This is the full Fabric transaction lifecycle!
//
app.post('/api/assets', async (req, res) => {
    try {
        const { contract } = await getContract();
        const { id, color, size, owner, appraisedValue } = req.body;

        if (!id || !color || !size || !owner || !appraisedValue) {
            return res.status(400).json({
                success: false,
                error: 'Missing required fields: id, color, size, owner, appraisedValue'
            });
        }

        // All arguments must be strings for chaincode
        await contract.submitTransaction(
            'CreateAsset',
            id,
            color,
            String(size),
            owner,
            String(appraisedValue)
        );

        res.json({ success: true, message: `Asset ${id} created and committed to ledger` });
    } catch (err) {
        res.status(500).json({ success: false, error: err.message });
    }
});

// ── PUT /api/assets/:id/transfer  →  TransferAsset ───────────────────────────
app.put('/api/assets/:id/transfer', async (req, res) => {
    try {
        const { contract } = await getContract();
        const { newOwner } = req.body;

        if (!newOwner) {
            return res.status(400).json({ success: false, error: 'Missing required field: newOwner' });
        }

        const resultBytes = await contract.submitTransaction(
            'TransferAsset',
            req.params.id,
            newOwner
        );

        // TransferAsset returns the previous owner name
        const previousOwner = Buffer.from(resultBytes).toString() || 'unknown';
        res.json({
            success: true,
            message: `Asset ${req.params.id} transferred from ${previousOwner} to ${newOwner}`
        });
    } catch (err) {
        res.status(500).json({ success: false, error: err.message });
    }
});

