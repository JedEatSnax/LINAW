'use strict';

/**
 * ============================================================
 *  server.js  —  Fabric Gateway Express Backend
 * ============================================================
 *
 *  ARCHITECTURE OVERVIEW
 *  ─────────────────────
 *  Browser  ──HTTP/REST──▶  Express (this file)  ──gRPC──▶  Fabric Peer
 *
 *  WHY A BACKEND SERVER?
 *  The @hyperledger/fabric-gateway SDK runs in Node.js only.
 *  It cannot run directly in a browser because it needs:
 *    1. Access to the filesystem (TLS certs, user identity files)
 *    2. The gRPC protocol, which browsers don't support natively
 *  So this server acts as a "bridge" between your browser UI
 *  and the Fabric network.
 *
 *  KEY FABRIC CONCEPTS DEMONSTRATED HERE
 *  ──────────────────────────────────────
 *  • gRPC Connection  : Low-level TLS-encrypted channel to the peer
 *  • Identity         : Who you are (certificate + private key from MSP)
 *  • Gateway          : The main entry point to Fabric operations
 *  • Network          : Represents a channel (e.g. "mychannel")
 *  • Contract         : A deployed chaincode (e.g. "basic")
 *  • evaluate()       : READ  — queries the ledger, NOT committed to chain
 *  • submit()         : WRITE — endorsed, ordered, committed to chain
 *  • Chaincode Events : Async notifications from chaincode after commit
 * ============================================================
 */

